package com.example.terz99.whiteboard;

public class Coordinates {
    private float mX;
    private float mY;

    public Coordinates(float mX, float mY) {
        this.mX = mX;
        this.mY = mY;
    }

    public float getmX() {
        return mX;
    }

    public float getmY() {
        return mY;
    }
}
